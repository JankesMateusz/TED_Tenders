export const getCpvName = (code: string): string => {
    // First try exact match
    if (cpvMapping[code]) {
        return cpvMapping[code];
    }

    // If no exact match, try to find the closest parent code
    const baseCode = code.split('-')[0];
    for (let i = baseCode.length; i >= 2; i -= 2) {
        const parentCode = baseCode.substring(0, i);
        const matchingKey = Object.keys(cpvMapping).find(key => key.startsWith(parentCode));
        if (matchingKey) {
            return cpvMapping[matchingKey];
        }
    }

    return code; // Return the original code if no match found
}; 