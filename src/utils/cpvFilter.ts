// Function to check if a tender CPV code is IT-related
export const isItRelatedCpv = (code: string): boolean => {
    if (!code) return false;
    const baseCode = code.split('-')[0];
    return cpvCodes.some(cpvCode => {
        // Check if the filter code has a suffix
        if (cpvCode.includes('-')) {
            // If filter code has suffix, split it and compare base codes
            const filterBase = cpvCode.split('-')[0];
            return baseCode.startsWith(filterBase);
        } else {
            // If filter code has no suffix, compare directly
            return baseCode.startsWith(cpvCode);
        }
    });
};

// Function to filter out change notices
export const removeChangeNotices = <T extends { changeNoticeVersionIdentifier?: string | null }>(tenders: T[]): T[] => {
    return tenders.filter(tender => !tender.changeNoticeVersionIdentifier);
};

// Function to filter tenders by CPV codes
export const filterTendersByCpvCodes = <T extends { cpvCodes: string[] }>(tenders: T[]): T[] => {
    return tenders.filter(tender => {
        return tender.cpvCodes.some((tenderCpv: string) => isItRelatedCpv(tenderCpv));
    });
}; 